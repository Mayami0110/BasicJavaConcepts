package loopConcepts;

public class aForLoop {

	public static void main(String[] args) {
		
		//Loops : concept which allow to run certain peice of code multiple time
		//1.for
		//2.while
		//3.do while
		//4. enhanced or for each after java 1.5
		
		//for(initialize ; condition ; increment/decrement)
		//{}
	//reserve keywords starts in small letter and different color
		for(int i=0;i<20;i++) {
			
			System.out.println(i);
			
		}
		System.out.println("loop ends"); //loop ends here
		
	}

}
